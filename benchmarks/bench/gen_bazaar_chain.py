#!/usr/bin/env python3
"""Vendor the bazaar-chain fixture (0.13 Phase 3): a wants-gated barter
world where depth-k goals force k-hop trade-up chains.

12 holders x 40 items, like the any-for-any bazaar, but a vendor releases
goods ONLY for the item they want, so (has a0 item-k) forces the chain
item0 -> item1 -> ... -> item-k through the right vendors, exactly k
trades. Vendors hold junk alongside their chain item, so every correct
trade competes with wrong ?y picks (branching); junk is wanted by NOBODY,
so a wrong pick strands the chain item — a dead end search must dodge.
Fully deterministic — no RNG.

The `x2` variant is the CONTENDED fixture: two chains (itemA*, itemB*)
cross at every vendor — v-j holds A-j AND B-j and wants A-(j-1) AND
B-(j-1) — and both chains feed from one mind's two seeds. The delete
relaxation cannot see the contention (a relaxed trade never spends the
offered item, so one offer "advances" both chains at once); real search
must dodge cross-picks that strand a chain. This is where the think
curves get their texture; the solo fixture is heuristic-transparent
(recorded in benchmarks/bazaar-thinks.md).

Run: python3 gen_bazaar_chain.py > bazaar-chain.pddl
     python3 gen_bazaar_chain.py x2 > bazaar-chain-x2.pddl
"""

import sys

CHAIN = 11  # chain items item1..item11, ONE per vendor; item0 is the seed
JUNK = 28  # item12..item39: wrong-pick bait — nobody wants junk, so a
# mis-traded chain item is gone and the state is dead (search must avoid
# or back out, which is the point)
VENDORS = 11  # v1..v11; a0 is the thinking NPC


def holder_of(j: int) -> str:
    return f"v{(j - 1) % VENDORS + 1}"


def main() -> None:
    contended = len(sys.argv) > 1 and sys.argv[1] == "x2"
    chains = ["itemA", "itemB"] if contended else ["item"]
    n_junk = 40 - len(chains) * (CHAIN + 1)
    has = {f"v{i}": [] for i in range(1, VENDORS + 1)}
    wants = {f"v{i}": [] for i in range(1, VENDORS + 1)}
    for c in chains:
        for j in range(1, CHAIN + 1):
            has[holder_of(j)].append(f"{c}{j}")
            wants[holder_of(j)].append(f"{c}{j - 1}")
    for t in range(n_junk):
        has[f"v{t % VENDORS + 1}"].append(f"junk{t}")

    objs_h = " ".join(["a0"] + [f"v{i}" for i in range(1, VENDORS + 1)])
    items = [f"{c}{j}" for c in chains for j in range(CHAIN + 1)]
    items += [f"junk{t}" for t in range(n_junk)]
    objs_i = " ".join(items)
    init = [f"(has a0 {c}0)" for c in chains]
    for v in sorted(has, key=lambda s: int(s[1:])):
        init += [f"(has {v} {x})" for x in has[v]]
        init += [f"(wants {v} {x})" for x in wants[v]]
    name = "bazaar-chain-x2" if contended else "bazaar-chain"
    goal = (
        f"(and (has a0 itemA{CHAIN}) (has a0 itemB{CHAIN}))"
        if contended
        else f"(has a0 item{CHAIN})"
    )
    print(f""";; wants-gated barter at bazaar scale (generated by gen_bazaar_chain.py
;; — regenerate rather than hand-edit). Goal depth k = swap the goal to
;; the depth-k item(s); the wants chain forces k trades per chain.
(define (problem {name}) (:domain bazaar-chain)
  (:objects {objs_h} - holder
            {objs_i} - item)
  (:init {' '.join(init)})
  (:goal {goal}))""")


if __name__ == "__main__":
    main()
