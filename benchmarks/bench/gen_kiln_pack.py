#!/usr/bin/env python3
"""kiln-pack fixture family (0.15 Phase 1) — the window-packing shape of
temporal-machine-shop at controllable scale, WITHOUT its symmetry (unary
goals form no orbits) so the window mechanism is isolated.

One kiln cycles cold -> fire(8) -> cold; each job needs TWO bake stages
(precedence: bake1 before bake2), every stage holds one of TWO kiln
slots for its duration and needs (ready) over all. Doom-pruning (0.14)
already kills window-OVERRUN starts at birth; what remains, and what the
probe must attribute, is packing: which job/stage gets a slot in this
window, when to refire, and how the plateau grows with N. Slot-time is
6 per job vs 16 per window, so windows-needed grows with N and no
single window swallows the instance.

Usage: gen_kiln_pack.py [sizes...]   (default: 2 4 6 8 10 12)
Writes kiln-pack-domain.pddl + kiln-pack-N.pddl next to this script.
"""
import os
import sys

HERE = os.path.dirname(os.path.abspath(__file__))

DOMAIN = """;; kiln-pack (0.15 Phase 1): window packing, bounded capacity, staged jobs.
;; Generated by gen_kiln_pack.py — edit that, not this.
(define (domain kiln-pack)
  (:requirements :typing :durative-actions :numeric-fluents)
  (:types job slot)
  (:predicates (ready) (cold) (free ?s - slot)
               (raw ?j - job) (mid ?j - job) (baked ?j - job))
  (:functions (bake-time ?j - job))
  (:durative-action fire
    :parameters ()
    :duration (= ?duration 8)
    :condition (at start (cold))
    :effect (and (at start (not (cold))) (at start (ready))
                 (at end (not (ready))) (at end (cold))))
  (:durative-action bake1
    :parameters (?j - job ?s - slot)
    :duration (= ?duration (bake-time ?j))
    :condition (and (at start (raw ?j)) (at start (free ?s))
                    (over all (ready)))
    :effect (and (at start (not (raw ?j))) (at start (not (free ?s)))
                 (at end (free ?s)) (at end (mid ?j))))
  (:durative-action bake2
    :parameters (?j - job ?s - slot)
    :duration (= ?duration (bake-time ?j))
    :condition (and (at start (mid ?j)) (at start (free ?s))
                    (over all (ready)))
    :effect (and (at start (not (mid ?j))) (at start (not (free ?s)))
                 (at end (free ?s)) (at end (baked ?j)))))
"""


def instance(n: int) -> str:
    jobs = " ".join(f"j{i}" for i in range(1, n + 1))
    init = ["(cold)", "(free s1)", "(free s2)"]
    for i in range(1, n + 1):
        init.append(f"(raw j{i})")
        init.append(f"(= (bake-time j{i}) {3 if i % 2 else 5})")
    goal = " ".join(f"(baked j{i})" for i in range(1, n + 1))
    return f""";; kiln-pack size {n}: {n} two-stage jobs (durations alternate 3/5)
;; through 8-long kiln windows with 2 slots. Generated by gen_kiln_pack.py.
(define (problem kiln-pack-{n})
  (:domain kiln-pack)
  (:objects {jobs} - job s1 s2 - slot)
  (:init {' '.join(init)})
  (:goal (and {goal}))
  (:metric minimize (total-time)))
"""


def main() -> None:
    sizes = [int(a) for a in sys.argv[1:]] or [2, 4, 6, 8, 10, 12]
    with open(os.path.join(HERE, "kiln-pack-domain.pddl"), "w") as f:
        f.write(DOMAIN)
    for n in sizes:
        with open(os.path.join(HERE, f"kiln-pack-{n}.pddl"), "w") as f:
            f.write(instance(n))
    print(f"wrote domain + {len(sizes)} instances: {sizes}")


if __name__ == "__main__":
    main()
